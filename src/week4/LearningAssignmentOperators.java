package week4;

public class LearningAssignmentOperators {

	public static void main(String[] args) {
		// Add and Assign
		int a = 5;
		System.out.println(a);
		a += 10; // Same as a = a + 10
		System.out.println(a);
		System.out.println();
		
		// Subtract and Assign
		int b = 5;
		System.out.println(b);
		b -= 10; // Same as b = b - 10
		System.out.println(b);
		System.out.println();
		
		// Multiply and Assign
		int c = 5;
		System.out.println(c);
		c *= 10; // Same as c = c * 10
		System.out.println(c);
		System.out.println();
		
		// Divide and Assign
		int d = 50;
		System.out.println(d);
		d /= 10; // Same as d = d / 10
		System.out.println(d);
		
	}
	
}
