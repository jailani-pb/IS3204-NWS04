package week4;

public class LearningSpecialCharacters {

	public static void main(String[] args) {
		System.out.println("Hello World");
		// Tab
		System.out.println("\tHello World");
		System.out.println("\t\t\tHello World");
		System.out.println("Hello\t\tJailani");
		System.out.println("Hi\t\tRahman");
		// Newline
		System.out.println("Hello World\n");
		System.out.println("Jailani\nRahman");
		// Printing double quotes
		System.out.println("\"This is a quote\"");
		// Printing backward slash
		System.out.println("\\ This is a backward slash");
	}
	
}
